function printResult1(){
    var active = document.querySelector(".select-btn")

    var btn1 = document.querySelector("#clickResults1")
    var table1 = document.querySelector("#table1")
    var ex1 = document.querySelector("#ex1");
    var count = 0
    ex1.addEventListener("click", () => {
        // Tăng giá trị biến đếm
        count++;
      
        // Kiểm tra trạng thái của biến đếm
        if (count % 2 === 1) {
          // Số lẻ: hiển thị phần tử
          table1.style.display = "block";
          btn1.style.display = "block";
          active.classList.add("active")
        } else {
          // Số chẵn: ẩn phần tử
          table1.style.display = "none";
          btn1.style.display = "none";
          active.classList.remove("active")
        }
      });
}
printResult1()

